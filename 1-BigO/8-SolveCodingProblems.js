function containsCommonItem1(arr1, arr2) {
  // پیچیدگی زمانی ساخت Set از آرایه اول: O(n)
  const set = new Set(arr1);

  // پیمایش آرایه دوم و بررسی وجود عنصر در Set: O(m)
  for (let item of arr2) {
    if (set.has(item)) {
      return true; // در صورت پیدا شدن عضو مشترک، بازگشت سریع
    }
  }

  // مجموع پیچیدگی زمانی این تابع: O(n + m)
  return false;
}

function containsCommonItem2(arr1, arr2) {
  // پیمایش دوبه‌دو آرایه‌ها با استفاده از دو حلقه تودرتو
  for (let i = 0; i < arr1.length; i++) {
    for (let j = 0; j < arr2.length; j++) {
      if (arr1[i] === arr2[j]) {
        return true; // در صورت پیدا شدن عضو مشترک، بازگشت سریع
      }
    }
  }

  // مجموع پیچیدگی زمانی این تابع: O(n × m)
  return false; // اگر هیچ عضو مشترکی پیدا نشد
}

function containsCommonItem3(arr1, arr2) {
  const map = {};

  // ذخیره اعضای آرایه اول در یک شیء (Map-like structure): O(n)
  for (let i = 0; i < arr1.length; i++) {
    map[arr1[i]] = true;
  }

  // پیمایش آرایه دوم و بررسی وجود عضو در شیء: O(m)
  for (let j = 0; j < arr2.length; j++) {
    if (map[arr2[j]]) {
      return true; // در صورت پیدا شدن عضو مشترک، بازگشت سریع
    }
  }

  // مجموع پیچیدگی زمانی این تابع: O(n + m)
  return false;
}

function containsCommonItem4(arr1, arr2) {
  // تابع some آرایه اول را پیمایش می‌کند: O(n)
  // تابع includes برای هر عنصر آرایه دوم را جستجو می‌کند: O(m)
  // مجموع پیچیدگی زمانی در بدترین حالت: O(n × m)
  return arr1.some((item) => arr2.includes(item));
}

