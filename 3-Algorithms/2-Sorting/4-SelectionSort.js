/*
=====================================================================================================================================================================
=====================================================================================================================================================================
 
Selection Sort : یکی از ساده‌ترین الگوریتم‌های مرتب‌سازی است این الگوریتم بر اساس ایده انتخاب کوچک‌ترین یا بزرگ‌ترین عنصر در هر مرحله و قرار دادن آن در مکان درست عمل می‌کند

است زیرا همیشه نیاز به دو حلقه تو در تو دارد O(n ^2 ) در تمام حالات دارای پیچیدگی زمانی Selection Sort الگوریتم 

معمولاً تعداد کمتری از جابه‌جایی‌ها انجام می‌دهد Bubble Sort، Selection Sort  در مقایسه با 

=====================================================================================================================================================================

Selection Sort  نحوه عملکرد ==================================================================================================================================================

1 - آرایه را پیمایش کنید و کوچک‌ترین (یا بزرگ‌ترین) عنصر را پیدا کنید

2 - کوچک‌ترین (یا بزرگ‌ترین) عنصر را با عنصر موجود در موقعیت فعلی (ابتدای زیربخش مرتب‌نشده) جابه‌جا کنید

3 - به سراغ بخش باقی‌مانده آرایه بروید و مراحل را تکرار کنید

4 - این کار را تا زمانی ادامه دهید که آرایه به‌طور کامل مرتب شود

=====================================================================================================================================================================

پیاده‌سازی در جاوااسکریپت

*/

function selectionSort(arr) {
  const n = arr.length;

  for (let i = 0; i < n - 1; i++) {
    // پیدا کردن اندیس کوچک‌ترین مقدار در زیربخش مرتب‌نشده
    let minIndex = i;

    for (let j = i + 1; j < n; j++) {
      if (arr[j] < arr[minIndex]) {
        minIndex = j; // به‌روزرسانی اندیس کوچک‌ترین مقدار
      }
    }

    // جابه‌جا کردن کوچک‌ترین مقدار با عنصر در موقعیت i
    if (minIndex !== i) {
      [arr[i], arr[minIndex]] = [arr[minIndex], arr[i]];
    }
  }

  return arr; // بازگرداندن آرایه مرتب‌شده
}

// مثال:
const numbers = [64, 25, 12, 22, 11];
console.log(selectionSort(numbers));
// خروجی: [11, 12, 22, 25, 64]

